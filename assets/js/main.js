import "./card-category.js";

console.log('This site was generated by Hugo.');

var darkMode = localStorage.getItem('darkmode') || 'false';

(function() {
    if (darkMode == 'true') {
        document.documentElement.dataset.theme = 'dark';
    }
})()

document.addEventListener('DOMContentLoaded', () => {
    // hamburger menu
    const hamburger = document.getElementById('hamburger');
    const navMenu = document.querySelector('nav');

    hamburger.addEventListener('click', () => {
        hamburger.classList.toggle('active');
        navMenu.classList.toggle('active');
    });

    const dropdownTriggers = document.querySelectorAll('nav li > a');

    dropdownTriggers.forEach((link) => {
        const submenu = link.nextElementSibling;

        if (submenu && submenu.tagName === 'UL') {
            link.addEventListener('click', (e) => {
                if (window.getComputedStyle(hamburger).display !== 'none') {
                    e.preventDefault();
                    link.parentElement.classList.toggle('expanded');
                }
            });
        }
    });

    // header scroll
    const header = document.querySelector('header');

    const handleScroll = () => {
        if (window.scrollY > 50) {
            header.classList.add('is-scrolled');
        } else {
            header.classList.remove('is-scrolled');
        }
    };

    window.addEventListener('scroll', handleScroll);

    // Dark Mode Toggle

    const buttonsDarkMode = document.querySelectorAll('.dark-toggle');

    const toggleDarkMode = () => {
        darkMode = darkMode != 'true' ? 'true' : 'false';
        if (darkMode == 'true') {
            document.documentElement.dataset.theme = 'dark';
        } else {
            document.documentElement.dataset.theme = 'light';
        }
        localStorage.setItem('darkmode', darkMode);
    };

    buttonsDarkMode.forEach(btn => {
        btn.addEventListener('click', e => {
            toggleDarkMode();
            console.log("mengubah theme ke" + darkMode)
        });
    })
});
